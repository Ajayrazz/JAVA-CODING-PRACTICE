import java.util.*;
public class Product_a_and_b {
    public static void main (String args[]) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter value : ");
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = a * b;
        System.out.println("Product of a and b : " + c);
    }
}
