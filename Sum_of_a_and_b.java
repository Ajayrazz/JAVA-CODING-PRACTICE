import java.util.*;
public class Sum_of_a_and_b {
    public static void main (String args[]) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter value : ");
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = a + b;
        System.out.println("Sum : " + c);
    }
}
