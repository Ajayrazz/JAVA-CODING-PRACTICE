import java.util.ArrayList;
public class ArrayList_Add_Index_Element_Operation {
    public static void main(String args[]){
        ArrayList<Integer> list = new ArrayList<>();

        //Add Operaton
        list.add(1);
        list.add(2);
        list.add(3);
        list.add(4);
        list.add(5);
        System.out.println(list);

        //Add(index,element) Operation
        list.add(3,9);
        System.out.println(list);
    }
}
